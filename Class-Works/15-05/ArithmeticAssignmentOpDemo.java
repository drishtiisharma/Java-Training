
class ArithmeticAssignmentOpDemo
{
public static void main(String args[])
{
int a = 10;
int b = 20;


//a = a+b;
a +=b;
System.out.println("a +=b = "+a);

//a = a-b;
a -=b;
System.out.println("a -=b = "+a);

//a = a*b;
a *=b;
System.out.println("a *=b = "+a);

//a = a/b;
a /=b;
System.out.println("a /=b = "+a);

//a = a%b;
a %=b;
System.out.println("a %=b = "+a);
}
}
