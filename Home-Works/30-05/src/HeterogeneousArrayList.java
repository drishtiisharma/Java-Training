import java.util.ArrayList;

public class HeterogeneousArrayList {

	public static void main(String[] args) {
		ArrayList p=new ArrayList();
		p.add("rectangle");
		p.add(12);
		p.add("square");
		p.add(4);
		p.add("circle");
		p.add(3.14);
		System.out.println("Heterogeneous ArrayList:\n"+p);

	}

}
