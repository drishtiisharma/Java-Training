import java.util.LinkedList;

public class HomogeneousLinkedList {

	public static void main(String[] args) {
		LinkedList<Double> d=new LinkedList<Double>();
		d.add(3.14);
		d.add(12.365);
		d.add(10.21);
		d.add(52.4654);
		d.add(50.5);
		d.add(70.54);
		System.out.println("Homogeneous Linked List:\n"+d);

	}

}
